  let theme_dark = false;

  function switchTheme() {
    theme_dark = !theme_dark;
    if (theme_dark) {
      document.documentElement.classList.add('dark');
      localStorage.setItem('color-theme', 'dark');
    } else {
      document.documentElement.classList.remove('dark');
      localStorage.removeItem('color-theme');
    }
  }
  //autoload theme from localstorage
  if (localStorage.getItem('color-theme') === 'dark') {
    document.documentElement.classList.add('dark');
    theme_dark = true;
  } else {
    document.documentElement.classList.remove('dark');
    theme_dark = false;
  }

  let props = $$props;
  let button_class = '';
  let svg_class = '';
  if (props.icon_style == 'hasbg') {
    button_class = 'bg-gray-700 bg-opacity-50 focus:bg-slate-500 focus:bg-opacity-50 h-8 w-8';
    svg_class = 'text-white';
  } else {
    button_class = 'bg-transparent h-10 w-10';
    svg_class = 'text-slate-400';
  }
  // export let icon_style = '';
</script>

